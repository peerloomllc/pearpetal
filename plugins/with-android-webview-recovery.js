// Expo config plugin: GrapheneOS/Vanadium WebView resume-freeze recovery.
//
// Symptom (from 2026-07-19): on GrapheneOS, returning to a backgrounded WebView
// app leaves the screen frozen - taps and haptics still work, nothing repaints -
// until the app is swiped away and reopened.
//
// Root cause (proven from live logcat on the Pixel, see
// /home/tim/peerloomllc/WEBVIEW_FREEZE_FIX_PORT.md): Android's cached-app freezer
// cgroup-freezes the WebView's out-of-process Vanadium renderer while the app is
// backgrounded. Since Vanadium 151.0.7922 the thawed renderer's compositor never
// re-attaches to the NEW window surface the app gets on resume, so it produces
// zero buffers. React/JS/input are fine - they live in the app process.
//
// Fix: force a FRESH render process on resume-after-background. A view-remount
// does NOT work (it rebinds the same pooled, stale renderer); only a new render
// process recovers it, which is what dismiss+reopen does.
// WebViewRenderProcess.terminate() (API 29+, and minSdk here is 29) terminates
// just this app's renderer; the shell's onRenderProcessGone then reloads, and
// the fresh renderer binds the current surface and paints.
//
// This lives in a config plugin rather than in android/ because android/ is
// gitignored and regenerated from app.json + plugins on every `expo prebuild`,
// so a hand-written Kotlin file would be wiped on the next prebuild. Reference
// implementation: PearCircle PR #165 (which checks android/ in, so it edits the
// Kotlin directly). Always on, debug and release, per the 2026-07-21 decision.

const path = require('path')
const fs = require('fs')
const { withDangerousMod, withMainApplication } = require('expo/config-plugins')

const MODULE_KT = (pkg) => `package ${pkg}

import android.os.Build
import android.view.View
import android.view.ViewGroup
import android.webkit.WebView
import com.facebook.react.bridge.Promise
import com.facebook.react.bridge.ReactApplicationContext
import com.facebook.react.bridge.ReactContextBaseJavaModule
import com.facebook.react.bridge.ReactMethod

// GENERATED by plugins/with-android-webview-recovery.js - do not edit here, the
// next \`expo prebuild\` overwrites it.
//
// GrapheneOS/Vanadium WebView resume-freeze recovery. The cached-app freezer
// freezes the out-of-process Vanadium renderer while backgrounded; on resume its
// compositor never re-attaches to the new window surface, so it never repaints.
// Only a FRESH render process recovers it. WebViewRenderProcess.terminate()
// (API 29+, minSdk 29) terminates just this app's renderer; the JS
// onRenderProcessGone handler then reloads a fresh one bound to the current
// surface. A view-remount does NOT work (it reuses the pooled stale renderer).
class WebViewRecoveryModule(ctx: ReactApplicationContext) : ReactContextBaseJavaModule(ctx) {

    override fun getName() = "WebViewRecovery"

    @ReactMethod
    fun terminateRenderer(promise: Promise) {
        val activity = reactApplicationContext.currentActivity
        if (activity == null) { promise.resolve(0); return }
        activity.runOnUiThread {
            try {
                if (Build.VERSION.SDK_INT < Build.VERSION_CODES.Q) { promise.resolve(0); return@runOnUiThread }
                val root = activity.window?.decorView
                var terminated = 0
                for (wv in findWebViews(root)) {
                    if (wv.webViewRenderProcess?.terminate() == true) terminated++
                }
                promise.resolve(terminated)
            } catch (e: Throwable) { promise.reject("terminate_failed", e) }
        }
    }

    private fun findWebViews(view: View?): List<WebView> {
        if (view == null) return emptyList()
        val out = ArrayList<WebView>()
        val stack = ArrayDeque<View>()
        stack.addLast(view)
        while (stack.isNotEmpty()) {
            val v = stack.removeLast()
            if (v is WebView) out.add(v)
            if (v is ViewGroup) for (i in 0 until v.childCount) stack.addLast(v.getChildAt(i))
        }
        return out
    }
}
`

const PACKAGE_KT = (pkg) => `package ${pkg}

import com.facebook.react.ReactPackage
import com.facebook.react.bridge.NativeModule
import com.facebook.react.bridge.ReactApplicationContext
import com.facebook.react.uimanager.ViewManager

// GENERATED by plugins/with-android-webview-recovery.js - do not edit here, the
// next \`expo prebuild\` overwrites it.
//
// PearPetal has no other hand-written native module on Android (the one custom
// module, local-network, is iOS-only and ships as an Expo module), so this
// package exists solely to register WebViewRecoveryModule. MainApplication's
// getPackages() adds it.
class WebViewRecoveryPackage : ReactPackage {
    override fun createNativeModules(reactContext: ReactApplicationContext): List<NativeModule> =
        listOf(WebViewRecoveryModule(reactContext))

    override fun createViewManagers(reactContext: ReactApplicationContext): List<ViewManager<*, *>> =
        emptyList()
}
`

function withWebViewRecoverySource (config) {
  return withDangerousMod(config, ['android', (cfg) => {
    const pkg = cfg.android?.package
    if (!pkg) throw new Error('with-android-webview-recovery: android.package is not set in app.json')
    const dir = path.join(cfg.modRequest.platformProjectRoot, 'app/src/main/java', ...pkg.split('.'))
    fs.mkdirSync(dir, { recursive: true })
    fs.writeFileSync(path.join(dir, 'WebViewRecoveryModule.kt'), MODULE_KT(pkg))
    fs.writeFileSync(path.join(dir, 'WebViewRecoveryPackage.kt'), PACKAGE_KT(pkg))
    return cfg
  }])
}

function withWebViewRecoveryRegistered (config) {
  return withMainApplication(config, (cfg) => {
    let contents = cfg.modResults.contents
    if (contents.includes('WebViewRecoveryPackage()')) return cfg
    // The Expo template leaves a commented `add(MyReactNativePackage())` hint
    // inside PackageList(this).packages.apply { ... }. Anchor on the apply block
    // itself rather than the comment, which is template text and could change.
    const anchor = /(PackageList\(this\)\.packages\.apply \{\n)/
    if (!anchor.test(contents)) {
      throw new Error('with-android-webview-recovery: could not find the getPackages() apply block in MainApplication.kt')
    }
    contents = contents.replace(anchor, '$1              add(WebViewRecoveryPackage())\n')
    cfg.modResults.contents = contents
    return cfg
  })
}

module.exports = function withAndroidWebViewRecovery (config) {
  return withWebViewRecoveryRegistered(withWebViewRecoverySource(config))
}
